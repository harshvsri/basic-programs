//Cheak for natural number.
#include<stdio.h>
int main()
{
    int num;
    printf("Enter a number : ");
    scanf("%d" , &num);
    if(num%1==0)
    {
        printf("NATURAL");
    }
    else if(num==0)
    {
        printf("NOT NATURAL");
    }
    else
    {
        printf("NOT NATURAL");
    }
}

