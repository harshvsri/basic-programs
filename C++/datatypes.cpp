#include<iostream>
using namespace std;
int main(void) {
    bool b;
    char c;
    int i;
    short int si;
    long int li;
    long long int lli;
    float f;

    cout<<"Size of bool is "<<sizeof(b)<<"."<<endl;
    cout<<"Size of char is "<<sizeof(c)<<"."<<endl;
    cout<<"Size of int is "<<sizeof(i)<<"."<<endl;
    cout<<"Size of short int is "<<sizeof(si)<<"."<<endl;
    cout<<"Size of long int is "<<sizeof(li)<<"."<<endl;
    cout<<"Size of long long int is "<<sizeof(lli)<<"."<<endl;
    cout<<"Size of float is "<<sizeof(f)<<"."<<endl;
}